#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	if (size > ft_strlen(src) + 1)
		ft_memcpy(dst, src, ft_strlen(src) + 1);
	else if (size > 0)
	{
		ft_memcpy(dst, src, size - 1);
		dst[size - 1] = '\0';
	}
	return (ft_strlen(src));
}
